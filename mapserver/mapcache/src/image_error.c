static int fontwidth = 6;
static int fontheight = 13;

#include "geocache.h"

static void renderGlyph(geocache_context *ctx, int glyph, geocache_image *im, int x, int y) {
   if(x>=0 && x<im->w-fontwidth && y>=0 && y<im->h-fontheight) {
      int i,j;
      char *bitmap = &fontData[glyph*fontwidth*fontheight];
      for(i=0;i<fontheight;i++) {
         unsigned char *rowptr = &im->data[(y+i)*im->stride+x*4];
         for(j=0;j<fontwidth;j++) {
            unsigned char *pixptr = &rowptr[j*4];
            if(bitmap[i*fontwidth+j]) {
               pixptr[0]=0;
               pixptr[1]=0;
               pixptr[2]=0;
               pixptr[3]=255;
            }
         }
      }
   }
}

static void writeErrorImage(geocache_context *ctx, geocache_image *im, char *txt) {
   int margin = 5;
   int txtlen = strlen(txt);
   int maxlines = im->h / fontheight - 1;
   int maxlinelength = im->w -2 * margin;
   char **lineends = apr_pcalloc(ctx->pool,maxlines*sizeof(char*));
   if(txtlen*fontwidth <= maxlinelength) {
      int hoffset = margin + (maxlinelength-txtlen*fontwidth)/2;
      int voffset = im->h/2 - fontheight/2;
      while(*txt) {
         renderGlyph(ctx,*txt,im,hoffset,voffset);
         hoffset+=fontwidth;
         txt++;
      }
   } else {
      char *strptr = txt;
      int curlinelength=0;
      int nlines = 1;

      /*find the positions of the spaces that we will use for line wrapping*/
      while(*strptr) {
         if(*strptr==' ') {
            if(curlinelength>maxlinelength) {
               strptr = lineends[nlines-1];
               nlines++;
               strptr++;
               curlinelength = 0;
            } else {
               curlinelength += fontwidth;
               lineends[nlines-1]=strptr;
               strptr++;
            }
         } else {
            curlinelength += fontwidth;
            strptr++;
         }
      }
      /*is the last line too long?*/
      if(curlinelength>maxlinelength) {
         nlines++;
      }

      int curline = 0;
      strptr=txt;
      int x,y;
      y = im->h/2 - nlines*(fontheight+2)/2 - 2;
      x=margin;
      while(*strptr) {
         if(strptr==lineends[curline]) {
            x = margin;
            y += fontheight + 2;
         } else {
            renderGlyph(ctx,*strptr,im,x,y);
            x+=fontwidth;
         }
         strptr++;
      }


   }
}

geocache_image *geocache_error_image(geocache_context *ctx, int width, int height, char *msg) {
   geocache_image *im = geocache_image_create(ctx);
   im->w = width;
   im->h = height;
   im->stride = width*4;
   im->data = apr_pcalloc(ctx->pool, width*height*4);
   writeErrorImage(ctx,im,msg);
   return im;
}
